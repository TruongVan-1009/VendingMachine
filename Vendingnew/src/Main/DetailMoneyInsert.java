public class DetailMoneyInsert {
    private double detail;
    private String time;

    public DetailMoneyInsert() {
    }

    public DetailMoneyInsert(double detail, String time) {
        this.detail = detail;
        this.time = time;
    }

    public double getDetail() {
        return detail;
    }

    public void setDetail(double detail) {
        this.detail = detail;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }
    
    
}
